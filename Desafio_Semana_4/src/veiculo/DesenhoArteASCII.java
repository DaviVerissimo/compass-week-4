package veiculo;

public abstract class DesenhoArteASCII {

	public static void desenharCarro() {
		System.out.println("        ______");
		System.out.println("  ___/|     ||");
		System.out.println(" |          ||");
		System.out.println("_ ========= ||");
		System.out.println(" ");
		System.out.println("----------------------");
		System.out.println(" ");
	}

	public static void desenharMoto() {
		System.out.println("     __o");
		System.out.println("  _ \\<_");
		System.out.println(" (_)/(_)");
		System.out.println(" ");
		System.out.println("----------------------");
		System.out.println(" ");
	}

	public static void desenharBicicleta() {
		System.out.println("     __o");
		System.out.println("  _ \\</_");
		System.out.println("   (_)/(_)");
		System.out.println(" ");
		System.out.println("----------------------");
		System.out.println(" ");
	}

	public static void desenharCharrete() {
		System.out.println("                                      ######      ");
		System.out.println("                                    ##########    ");
		System.out.println("                                  ##############  ");
		System.out.println("                                ##########  ######");
		System.out.println("                    ##        ##########          ");
		System.out.println("        ##################################        ");
		System.out.println("------####----############################        ");
		System.out.println("  ######      ############################        ");
		System.out.println("              ##########################          ");
		System.out.println("            ##########  ##################        ");
		System.out.println("          ##########                  ######      ");
		System.out.println("        ######      ##          ####      ##      ");
		System.out.println("        ####      ##            ####      ####    ");
		System.out.println("      ####        ####        ####          ##    ");
		System.out.println("      ####          ##    ####              ####  ");
		System.out.println("      ##              ##                      ##  ");
		System.out.println("                                                   ");
		System.out.println(" ");
		System.out.println("----------------------");
		System.out.println(" ");
	}
	
	public static void desenharCaminhao() {
		System.out.println("        ______    ##########");
		System.out.println("  ___/|     ||   #        #");
		System.out.println(" |          ||   #        #");
		System.out.println("_ ========= ||   #        #");
		System.out.println("|o           |   ##########");
		System.out.println(" ");
		System.out.println("----------------------");
		System.out.println(" ");
	}

}
